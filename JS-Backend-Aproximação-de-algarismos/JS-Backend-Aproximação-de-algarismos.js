//Math.round()
//Aproximação de um algarismo para o seu valor inteiro mais proximo
var idade = 3.6
Math.round(idade)
console.log(idade)

//Math.ceil
// Aproximação de um algarismo para o seu valor inteiro mais alto
var idade1 = 3.6
Math.ceil(idade1)
console.log(idade1)


//Math.floor
//Aproximação de um algarismo para o seu valor inteiro mais baixo
var idade2 = 3.6
Math.floor(idade2)
console.log(idade2)


//Math.trunc
//Desconsidera o valor decimal
var idade3 = 3.6
Math.trunc(idade3)
console.log(idade3)
